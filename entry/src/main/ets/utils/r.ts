/**
 * Random Utils
 *
 * - r: it is a random char. such as: console.log(r); // 'z'
 *
 * - r(8): get a random string. such as: console.log(r(8)); // 'zsdwqbcd'
 *
 * - r.time: get a random time.
 *
 * - r.time('d', 30): get a random time. (in 30 days.)
 *
 * - r.timeFuture: get a random future time.
 *
 * - r.timeFuture('d', 30): get a random time. (in 30 days.)
 *
 * - r.timeBefore: 随机获得一个以前的时间
 *
 * - r.timeBefore('d', 30)
 *
 * - r.number: 随机获得一个数字
 *
 * - r.number(1, 99)
 *
 * - r.oddNumber: 随机获得一个奇数
 *
 * - r.oddNumber(-7, 101)
 *
 * - r.evenNumber: 随机获得一个偶数
 *
 * - r.evenNumber(-5, 5)
 *
 */